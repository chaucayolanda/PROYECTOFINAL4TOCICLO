package Beans;

public class BeanLogin {

    private int _loginId;
    private String _usuario;
    private String _clave;
    private String _cargo;
    private String _codigo;

    /**
     * @return the _loginId
     */
    public int getLoginId() {
        return _loginId;
    }

    /**
     * @param _loginId the _loginId to set
     */
    public void setLoginId(int _loginId) {
        this._loginId = _loginId;
    }

    /**
     * @return the _usuario
     */
    public String getUsuario() {
        return _usuario;
    }

    /**
     * @param _usuario the _usuario to set
     */
    public void setUsuario(String _usuario) {
        this._usuario = _usuario;
    }

    /**
     * @return the _clave
     */
    public String getClave() {
        return _clave;
    }

    /**
     * @param _clave the _clave to set
     */
    public void setClave(String _clave) {
        this._clave = _clave;
    }

    /**
     * @return the _cargo
     */
    public String getCargo() {
        return _cargo;
    }

    /**
     * @param _cargo the _cargo to set
     */
    public void setCargo(String _cargo) {
        this._cargo = _cargo;
    }

    /**
     * @return the _codigo
     */
    public String getCodigo() {
        return _codigo;
    }

    /**
     * @param _codigo the _codigo to set
     */
    public void setCodigo(String _codigo) {
        this._codigo = _codigo;
    }
    
}
