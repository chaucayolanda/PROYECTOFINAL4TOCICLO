package Beans;

public class BeanCompras {

    private String CodCompra;
    private String CodProducto;
    private String Cantidad;
    private String PCompra;
    private String PVenta;
    private String Subtotal;
    private String Igv;
    private String Total;
    private String Estado;

    /**
     * @return the CodCompra
     */
    public String getCodCompra() {
        return CodCompra;
    }

    /**
     * @param CodCompra the CodCompra to set
     */
    public void setCodCompra(String CodCompra) {
        this.CodCompra = CodCompra;
    }

    /**
     * @return the CodProducto
     */
    public String getCodProducto() {
        return CodProducto;
    }

    /**
     * @param CodProducto the CodProducto to set
     */
    public void setCodProducto(String CodProducto) {
        this.CodProducto = CodProducto;
    }

    /**
     * @return the Cantidad
     */
    public String getCantidad() {
        return Cantidad;
    }

    /**
     * @param Cantidad the Cantidad to set
     */
    public void setCantidad(String Cantidad) {
        this.Cantidad = Cantidad;
    }

    /**
     * @return the PCompra
     */
    public String getPCompra() {
        return PCompra;
    }

    /**
     * @param PCompra the PCompra to set
     */
    public void setPCompra(String PCompra) {
        this.PCompra = PCompra;
    }

    /**
     * @return the PVenta
     */
    public String getPVenta() {
        return PVenta;
    }

    /**
     * @param PVenta the PVenta to set
     */
    public void setPVenta(String PVenta) {
        this.PVenta = PVenta;
    }

    /**
     * @return the Subtotal
     */
    public String getSubtotal() {
        return Subtotal;
    }

    /**
     * @param Subtotal the Subtotal to set
     */
    public void setSubtotal(String Subtotal) {
        this.Subtotal = Subtotal;
    }

    /**
     * @return the Igv
     */
    public String getIgv() {
        return Igv;
    }

    /**
     * @param Igv the Igv to set
     */
    public void setIgv(String Igv) {
        this.Igv = Igv;
    }

    /**
     * @return the Total
     */
    public String getTotal() {
        return Total;
    }

    /**
     * @param Total the Total to set
     */
    public void setTotal(String Total) {
        this.Total = Total;
    }

    /**
     * @return the Estado
     */
    public String getEstado() {
        return Estado;
    }

    /**
     * @param Estado the Estado to set
     */
    public void setEstado(String Estado) {
        this.Estado = Estado;
    }

}
