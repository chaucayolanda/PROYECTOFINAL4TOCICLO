package Beans;

public class BeanLoginCliente {

    private int _loginId;
    private String _dni;
    private String _clave;
    private String _codigo;

    /**
     * @return the _loginId
     */
    public int getLoginId() {
        return _loginId;
    }

    /**
     * @param _loginId the _loginId to set
     */
    public void setLoginId(int _loginId) {
        this._loginId = _loginId;
    }

    /**
     * @return the _dni
     */
    public String getDni() {
        return _dni;
    }

    /**
     * @param _dni the _dni to set
     */
    public void setDni(String _dni) {
        this._dni = _dni;
    }

    /**
     * @return the _clave
     */
    public String getClave() {
        return _clave;
    }

    /**
     * @param _clave the _clave to set
     */
    public void setClave(String _clave) {
        this._clave = _clave;
    }

    /**
     * @return the _codigo
     */
    public String getCodigo() {
        return _codigo;
    }

    /**
     * @param _codigo the _codigo to set
     */
    public void setCodigo(String _codigo) {
        this._codigo = _codigo;
    }

  
}